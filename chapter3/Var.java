package chapter3;

public class Var {

    public static void main(String[] args) {
        //====== List 3-1 ======
        int n;
        n = 4;
        System.out.println(2 * n + 3);

        //====== List 3-2 ======
        int x = 15;
        int y = 32;
        System.out.println((x + y) / 2);

        //====== List 3-3 ======
        double a = 15.0d;
        double b = 32.0d;
        System.out.println((a + b) / 2);

        //====== List 3-4 ======
        char c = 'A';
        char alpha = 'α';
        char hiroshi = '浩';

        System.out.println(c);
        System.out.println(alpha);
        System.out.println(hiroshi);

    }//main()

}//class

