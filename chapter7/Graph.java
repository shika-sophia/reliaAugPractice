  public static void main(String[] args) {
      int i = -10;

      while(i <= 10) {

        int j = 0;
        do {
            System.out.print("*");
            j++;
        } while(j < i * i / 10);

        j = 0;
        do  {
            System.out.print(" ");
            j++;
        } while(j < i * i / 10);

        j = 0;
        do {
            System.out.print("*");
            j++;
        } while(j < i * i / 10);

        System.out.println();
        i++;
    }//while i

  }//main()

